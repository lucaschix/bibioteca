public class Multa {

	private int monto;
	private int fecha;
	private String estado;

	public int getMonto() {
		return this.monto;
	}

	/**
	 * 
	 * @param monto
	 */
	public void setMonto(int monto) {
		this.monto = monto;
	}

	public int getFecha() {
		return this.fecha;
	}

	/**
	 * 
	 * @param fecha
	 */
	public void setFecha(int fecha) {
		this.fecha = fecha;
	}

	public String getEstado() {
		return this.estado;
	}

	/**
	 * 
	 * @param estado
	 */
	public void setEstado(String estado) {
		this.estado = estado;
	}

}